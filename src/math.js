/**
 *  Adds two numbers.
 *
 *  This is only for demonstrating some testing features.
 *
 * @param {number} x
 * @param {number} y
 * @return {number} x + y
 */
function addition(x, y) {
    return x + y;
}
exports.addition = addition;

/**
 * Subtracts two numbers.
 *
 * This is only for demonstrating some testing features.
 *
 * @param {number} x
 * @param {number} y
 * @return {number} x - y
 */
function subtraction(x, y) {
    return x - y;
}
exports.subtraction = subtraction;
